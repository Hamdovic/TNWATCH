    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   
     ResponsableDAO RD=new ResponsableDAO();
     CitoyensDAO CD=new CitoyensDAO();
     adminDAO AD=new adminDAO();
     int k=0;
    if(CD.authentification_citoyen(login.getText(),pwd.getText())){
        k=1;
    Citoyens b=CD.finduser(login.getText(),pwd.getText());  
    interface_user m=new interface_user(b.getLogin(),b.getSecurite(),b.getEmail(),b.getType_question(),b.getPwd(),b.getCin(),b.getNom(),b.getPrenom(),b.getDateNaissance(),b.getTelephone());
    m.setVisible(true);
    dispose();
    }
   
      if(RD.authentification_responsable(login.getText(),pwd.getText())){
          k=1;
          JOptionPane.showMessageDialog(null, "Bonjour responsable ");}
         if(AD.authentification_admin(login.getText(),pwd.getText()))
        {
            k=1;
            JOptionPane.showMessageDialog(null, "Bonjour Admin");
            System.out.print(pwd.getText()+login.getText());
        }
         else if(k==0) {JOptionPane.showMessageDialog(null, "Compte veroulle ou en cours de validation");  }
     
    }//GEN-LAST:event_jButton1ActionPerformed

